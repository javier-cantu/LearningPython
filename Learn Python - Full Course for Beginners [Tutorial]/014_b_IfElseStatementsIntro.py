# Else permite que se ejecute otra parte del codigo si no se cumple la condicion del if

# Declarar una boolean variable
is_daytime = False # Ahora es false para que no se cumple el if

# Asi se crea un if statement empezando por If
# Luego la condici√≥n
# En este caso si no ese cumple la condicion del if
# Se ejecuta el bloque de else
if is_daytime:
    print("Is daytime")
else: # else con :
    print("Is night time!")
